// responses/ext-content-gen.js
// Connector: ext-content-gen — Content Generator
// DU Cases: generate:summary, generate:outline, generate:report, generate:email, generate:minutes, analyze:summarize-eval
// Returns: Markdown string

'use strict';

function buildResponse(fields, files, filename) {
  const format = fields.format || 'paragraph';
  const audience = fields.audience || 'Chung';
  const tone = fields.tone || 'formal';
  
  // Note: Since we don't have the task explicitly in discriminator for generate,
  // we return a generic markdown response that covers most of the content generation needs.
  // In a real mock, we might inspect `prompt` to guess the task.

  let content = '';

  if (format === 'bullets') {
    content = `# Tóm Tắt (Dạng Bullets)

> **Thông tin:** Dành cho: ${audience} | Giọng điệu: ${tone} | File: \`${filename}\`

- **Mục tiêu chiến lược:** Đạt mốc 10.000 khách hàng hoạt động trong năm 2026.
- **Kế hoạch thực thi:** Tập trung nhóm bán hàng B2B và kênh tiếp thị trực tuyến.
- **Ngân sách dự kiến:** 1.5 tỷ VND cho marketing và 2.0 tỷ VND cho R&D.
- **Rủi ro:** Cạnh tranh nội địa tăng cao, cần ổn định chi phí chuỗi cung ứng.
- **Hành động tiếp theo:** Phê duyệt ngân sách trước ngày 15/04/2026.`;
  } else if (format === 'numbered') {
    content = `# Kế Hoạch / Outline

> **Thông tin:** Dành cho: ${audience} | Giọng điệu: ${tone} | File: \`${filename}\`

1. **Tổng Quan Dự Án**
   1.1. Bối cảnh thị trường
   1.2. Phân tích đối thủ
2. **Chiến Lược Kinh Doanh**
   2.1. Tối ưu giá thành sản phẩm
   2.2. Mở rộng hệ sinh thái số
3. **Phân Phối Ngân Sách**
   3.1. Kế hoạch năm 2026
   3.2. Dự phòng rủi ro
4. **Kết Luận & Đề Xuất**`;
  } else if (format === 'table') {
    content = `# Bảng Tổng Hợp Thông Tin

> **Thông tin:** Dành cho: ${audience} | Giọng điệu: ${tone} | File: \`${filename}\`

| Hạng Mục | Chi Tiết | Trạng Thái | Người Phụ Trách |
|----------|----------|------------|-----------------|
| Thiết kế UI/UX | Hoàn thiện luồng thanh toán | Đang làm | Nguyễn Văn A |
| Tích hợp API | Kết nối cổng VNPay | Hoàn thành | Trần Thị B |
| Kiểm thử hệ thống | Chạy test automation 100% case | Chưa bắt đầu| Đội QA |`;
  } else {
    // Default: paragraph (summary / report / email)
    content = `# Nội Dung Được Tạo Tự Động

> **Thông tin xử lý:**
> - File gốc: \`${filename}\`
> - Định dạng: **${format}**
> - Đối tượng đọc: **${audience}**
> - Giọng điệu: **${tone}**

---

**Kính gửi các bên liên quan,**

Dựa trên tài liệu \`${filename}\`, hệ thống đã tổng hợp những điểm cốt lõi nhất. Trọng tâm của tài liệu xoay quanh chiến lược phát triển sản phẩm mới và dự toán ngân sách cho năm tài chính 2026. 

Ban giám đốc nhấn mạnh tầm quan trọng của việc duy trì chất lượng dịch vụ trong khi vẫn đảm bảo tối ưu chi phí vận hành. Các mốc thời gian quan trọng cần lưu ý bao gồm hạn chót phê duyệt ngân sách vào cuối tháng 4 và bắt đầu triển khai dự án từ quý 3.

Đánh giá chung: Bản kế hoạch có tính khả thi cao, tuy nhiên cần làm rõ thêm phương án dự phòng cho các rủi ro liên quan đến biến động giá vật tư đầu vào.

Trân trọng,
*[MOCK] Generated by du-mock-service — ext-content-gen*`;
  }

  return {
    content,
    model: fields.model || 'gpt-4o',
    mock: true,
  };
}

module.exports = { buildResponse };
